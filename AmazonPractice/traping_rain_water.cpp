#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(ll len, vector<ll> v){
   vector<ll> left(len), right(len);
   ll mx = 0;
   for(int i = 0; i < len; i++){
      mx = max(mx, v[i]);
      left[i] = mx;
   }
   mx = 0;
   for(int i = len - 1; i >= 0; i--){
      mx = max(mx, v[i]);
      right[i] = mx;
   }

   ll sum  = 0;
   for(int i = 0; i < len; i++){
      sum += (min(left[i], right[i]) - v[i]);
   }
   for(auto i : left) cout << i << " "; cout << endl;
   for(auto i : right) cout << i << " "; cout << endl;
   cout << sum << endl;
}


int main() {
   ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll length;
    cin >> length;
    std::vector<ll> vec(length);
    for(int i = 0; i < length; i++){
      cin >> vec[i];
    }
    solve(length, vec);
    return 0;
}
