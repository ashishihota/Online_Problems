#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll n;
    cin >> n;
    ll ans = n, bm = 0, br = 0, gr = 0, gm = 0;
    string bri, grm;
    cin >> bri >> grm;
    for(int i = 0; i < n; i++){
        if(bri[i] == 'r') br++;
        else if(bri[i] == 'm') bm++;
        if(grm[i] == 'r') gr++;
        else if(grm[i] == 'm') gm++;
    }
    for(int i = 0; i < n; i++){
        if(bri[i] == 'm'){
            if(gm > 0) {
                gm--;
                ans--;
            }
            else {
                break;
            }
        }else{
            if(gr > 0){
                gr--;
                ans--;
            }
            else {
                break;
            }
        }
    }
    cout << ans << endl;    
    return 0;
}
