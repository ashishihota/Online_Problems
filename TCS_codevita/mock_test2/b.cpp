#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll n;
    cin >> n;
    vector<int> v(n);
    vector<int> vv(n);
    for(int i = 0; i < n; i++){
        ll mx = -1, mn = MOD;
        cin >> v[i];
        int nn = v[i];
        while(nn > 0){
            ll b = nn % 10;
            nn /= 10;
            mx = max(b, mx);
            mn = min(b, mn);
        }   
        ll ans = (mx * 11) + (mn * 7);
        ans %= 100;
        ll sub = ans % 10;
        ans -= sub;
        ans /= 10;
        vv[i] = ans;
    }
    multiset<int> odd, even;
    for(int i = 0; i < n; i++){
        ll ii  = vv[i];
        if(i % 2){
            odd.insert(ii);
        }else{
            even.insert(ii);
        }
    }
    set<pair<int,int>> sp;
    for(auto i : odd){
        sp.insert(make_pair(i, odd.count(i)));
    }
    for(auto i : even){
        sp.insert(make_pair(i, even.count(i)));
        } 
    int ans = 0;
    for(auto i: sp){
        if(i.second > 1){
            int nn = i.second + 1;
            ans += nn/2;
        }
    }
    cout << ans;
    return 0;
}
