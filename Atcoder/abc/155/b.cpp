using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    int n, m;
    cin >> n;
    bool flag = true;
    std::vector<int> v;
    for(int i = 0; i < n; i++){
        cin >> m;
        if(m % 2 == 0) v.push_back(m);
    }
    for(int i = 0; i < v.size(); i++){
        if((v[i] % 3 == 0) || (v[i] % 5 == 0)){
            continue;
        }
        else flag = false;
        break;
    }
    if(flag) cout << "APPROVED";
    else cout <<"DENIED";

    return 0;
}
