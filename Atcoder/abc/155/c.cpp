#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll n;
    int mx = -1;
    cin >> n;
    string s;
    map<string, int> mp;
    std::vector<string> v;
    for(int i = 0; i < n; i++){
        cin >> s;
        mp[s]++;
    }
    for(auto i : mp){
        int x = i.second;
        mx = max(mx, x);
    }
    for(auto i : mp){
        if(i.second == mx){
            v.push_back(i.first);
        }
    }
    for(auto i:v)cout << i << endl;
    return 0;
}
