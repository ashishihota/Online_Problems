#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll n, m = -1001001001, pos = -1;
    cin >> n;
    std::vector<ll> v(n + 1);
    for(int i = 1; i <= n; i++){
        cin >> v[i];
        if(v[i] > m) pos = i;
    }
    bool flag = false;
    ll sum = 1, tot = 10010010001;
    for(int j = 1; j <= 100; j++){
        sum = 0;
        for(int i = 1; i <= n; i++){
            ll ans = pow((v[i] - j), 2);
            sum += ans;
        }
        tot = min (sum, tot);
        if(sum > tot) break;
        //cout << "ans for j = " << j << " = "<< sum << endl;
    }

    cout << tot;
    return 0;
}
