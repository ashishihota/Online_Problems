#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int fact(int);

int nCr(int n, int r)
{
    return fact(n) / (fact(r) * fact(n - r));
}

// Returns factorial of n
int fact(int n)
{
    int res = 1;
    for (int i = 2; i <= n; i++)
        res = res * i;
    return res;
}

int main() {
    ll n, a, b;
    cin >> n >> a >> b;
    ll np, ap, bp;
    np = nCr(n);
    ap = nCr(a);
    bp = nCr(b);
    ll ans = np - (ap - bp);
    cout << ans % MOD;
    return 0;
}
