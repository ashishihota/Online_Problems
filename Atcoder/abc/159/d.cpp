using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


int main()
{
    int N;
    cin >> N;
    vector<int> A(N);
    cin >> A;
    map<int, lint> cnt;
    for (auto x : A) cnt[x]++;
    lint ret = 0;
    for (auto p : cnt) ret += (p.second - 1) * p.second / 2;
    REP(i, N) {
        lint n = cnt[A[i]];
        lint tmp = ret - (n - 1) * n / 2 + (n - 2) * (n - 1) / 2;
        cout << tmp << "\n";
    }
}

