#define repp(i,a,b) for(i=a;i<b;++i)

bool pal(string s){
    ll c = 0, n = s.length() -1;
    while(c < n){
        if(s[c] != s[n]) return false;
        c++;
        n--;
    }
    return true;
}

int main() {
    string s;
    bool flag  = true;
    cin >> s;
    ll len = s.length(), l1 = (len - 1)/2, l2 = (len + 3)/2;
    if(!pal(s)) flag =  false;
    string ss, sss;
    ss = s.substr(0, l1);
    sss = s.substr(l2 -1, len);
    //cout << ss << " " << sss << endl;
    if(!pal(ss)) {
        //cout << "false at 1"<< endl;
        flag = false;
    }
    if(!pal(sss)) {
        //cout << "false at 2" << endl;
        flag  = false;
    }
    if(flag) cout << "Yes";
    else cout << "No";
    return 0;
}
