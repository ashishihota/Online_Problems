#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
  ll t;
  cin >> t;
  while(t--){
    ll n, k;
    cin >> n >> k;
    vector<ll> v(n*k);
    for(int i = 0; i < n * k; i++){
      cin >> v[i];
    }
    ll mid = ceil(n/2);
    ll sum  = 0;
    ll len =  n * k - 1;
    for(int i = 1; i <= k; i++){
      sum += v[len - mid * i];
    }
    cout << sum << endl;
  }

  return 0;
}
