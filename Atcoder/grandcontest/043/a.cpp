#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll h, w, crs = 0;
    cin >> h >> w;
    char a[h][w];
    for(int i = 0; i < h; i++){
        for(int j = 0; j < w; j++){
            cin >> a[i][j];
            if(a[i][j] == '#') crs++;
        }
    }
    ll v[h][w];
    for(int i = 0; i < h; i++){
        for(int j = 0; j < w; j++){
            if(a[i][j] == '#') v[i][j] = 1;
            else v[i][j] = 0;
        }
    }
    ll sum[h][w];

    memset(sum, 0, sizeof(sum));
    sum[0][0] = v[0][0];
    for(int i = 1; i < h; i++){
        sum[i][0] = v[i][0] + v[i - 1][0];
    }
    for(int j = 1; j < w; j++){
        sum[0][j] = v[0][j] + v[0][j-1];
    }

    cout << endl;
    for(int i = 1; i <= h; i++){
        for(int j = 1; j <= w; j++){
            sum[i][j] = min(sum[i][j-1], sum[i-1][j]) + v[i][j];
        }
    }
    ll ans = sum[w - 1][h -1];
    // ll tot = (h * w) /2;
    // if((tot == crs) && (sum[0][0] == 0) ) ans ++;
    cout << ans;
    return 0;
}
