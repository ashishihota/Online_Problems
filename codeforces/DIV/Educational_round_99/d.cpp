#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n, k, kk;
  cin >> n >> k;
  vector<ll> v(n);
  for(auto& i : v) cin >> i;

  if(is_sorted(v.begin(), v.end())){
    cout << 0 << endl;
    return;
  }


  kk = k;
  for(int i = 1; i < n; i++){
    if(v[i] < v[i-1] && v[i] < kk){
      cout << -1 << endl;
      return;
    }
    else if(v[i] < v[i-1] && v[i] > kk){
      kk = v[i];
    }
  }

  ll cnt = 0;
  for(int i = 0; i < n; i++){
    if(is_sorted(v.begin(), v.end())){
      cout << cnt << endl;
      return;
    }
    if(v[i] <= k) continue;
    if(v[i] > k){
      ll temp = v[i];
      v[i] = k;
      k = temp;
      cnt++;
    }
  }

  if(is_sorted(v.begin(), v.end())){
    cout << cnt << endl;
  }
  else cout << -1 << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
