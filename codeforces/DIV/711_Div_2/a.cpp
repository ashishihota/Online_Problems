ll gcd(ll n){
	ll sum = 0;
	while(n > 0){
		sum += n%10;
		n /= 10;
	}
	return sum;
}

void solve(){
	ll n, m, sum = 0;
	cin >> n;
	sum = gcd(n);
	//cout << sum << " -- sum n -- " << n << endl;
	if(__gcd(gcd(n), n) > 1){
		cout << n << endl;
		return;
	}
	while(true){
		n++;
		if(__gcd(n, gcd(n)) > 1){
			cout <<n<< endl;
			return;
		} 
	}
}


