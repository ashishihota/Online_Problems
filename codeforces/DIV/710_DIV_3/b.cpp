#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
	ll n , k, first, last;
	cin >> n >> k;
	string s;
	cin >> s;
	for(int i = 0; i < n; i++){
		if(s[i] == '*'){
			first = i;
			break;
		}
	}
	for(int i = n - 1; i >= 0; i--){
		if(s[i] == '*') {
			last = i;
			break;
		}
	}

	if(last == first) {
		cout << 1 << endl;
		return;
	}

	ll tot = 0;
	ll prev = first, cnt = 0;

	for(int i = first; i < last; i++){
		if(s[i] == '.')cnt++;
		else cnt = 0;
		if(cnt == k){
			tot++;
			cnt = 0;
		}
	}

	ll need = ceil((last - first)/(k)) + 1;
	cout << "f l n " << first << " "<< last << " " << need<< endl;
	cout << need + tot << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
