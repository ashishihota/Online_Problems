#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  multiset<ll> ms;
  std::vector<ll> v(n);
  for(int i = 0; i < n; i++){
    cin >> v[i];
    ms.insert(v[i]);
  }

  ll prev =  -1;
  priority_queue<pair<ll,ll>> vp;
  for(auto i : ms){
    if(i == prev){
      continue;
    }
    else{
      vp.push({i, ms.count(i)});
    }
  }
  for(int i = 0; i < vp.size(); i++){
    pair<ll,ll> f = vp.top();
    vp.pop();
    pair<ll,ll> s = vp.top();
    vp.pop();
    if(f.second - s.second == 0){
      continue;
    }
    else{
      vp.push({f.first, (f.second - s.second)});
    }
  }
  cout << vp.size() << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
