#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while(t--){
      ll n;
      cin >> n;
      vector<ll> v(n);
      for(int i = 0; i < n; i++) cin >> v[i];
      sort(v.begin(), v.end());
      ll ans = -MOD, ans1 = 1;
      for(int i = 0; i < 5; i++){
        ans1 *= v[i];
      }
      ans = max(ans, ans1);
      ans1 = 1;
      for(int i = 0; i < 1; i++){
        ans1 *= v[i];
      }
      for(int i = 1; i < 5; i++){
        ans1 *= v[n-1-i];
      }
      ans = max(ans, ans1);
      ans1 = 1;
      for(int i = 0; i < 2; i++){
        ans1 *= v[i];
      }
      for(int i = 2; i < 5; i++){
        ans1 *= v[n-1-i];
      }
      ans = max(ans, ans1);
      ans1 = 1;
      for(int i = 0; i < 3; i++){
        ans1 *= v[i];
      }
      for(int i = 3; i < 5; i++){
        ans1 *= v[n-1-i];
      }
      ans = max(ans, ans1);
      ans1 = 1;
      for(int i = 0; i < 4; i++){
        ans1 *= v[i];
      }
      for(int i = 4; i < 5; i++){
        ans1 *= v[n-1-i];
      }
      ans = max(ans, ans1);
      ans1 = 1;
      for(int i = 0; i < 5; i++){
        ans1 *= v[i-1-i];
      }
      ans = max(ans, ans1);
      cout << ans << endl;
    }
    return 0;
}
