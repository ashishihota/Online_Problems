#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll f, s, t;
  cin >> f >> s >> t;
  ll mn = min(f,min(s,t));
  ll mx = max(f,max(s,t));
  ll sum = f + s + t;
  if(sum < 9) {
    cout << "NO" << endl;
    return;
  }
  if((sum - 3) % 6 != 0){
    cout << "NO" << endl;
    return;
  }
  if(mx - mn > mn * 6){
    cout << "NO" << endl;
    return;
  }
  if((sum % 6) % 3 == 0){
    cout << "YES" << endl;
    return;
  }
  else {
    cout << "NO" << endl;
  }
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
