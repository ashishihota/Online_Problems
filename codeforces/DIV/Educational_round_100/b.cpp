#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n, sum = 0;
  cin >> n;
  vector<ll> v(n);
  for(auto& i : v) {
    cin >> i;
  }
  ll cnt = 0;
  for(int i = 0; i < n; i++){
    if(v[i] == 1) cnt++;
    else
    sum += v[i];
  }
  sum /= (n-cnt);
  for(int i = 0; i < n; i++){
    if(v[i] == 1) cout << 1 << " ";
    else if(v[i] % sum == 0){
      cout << v[i] << " ";
    }
    else{
      ll sub = v[i] % sum;
      ll add = sum - sub;
      if(v[i] - sub <= 0) cout << v[i] + add << " ";
      else{
        cout << (add < sub ? v[i] + add : v[i] - sub) << " ";
      }
    }   
  }
  cout << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
