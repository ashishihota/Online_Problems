int main() {
    ll num;
    cin >> num;
    while(num--){
        ll a, b, c, n;
        cin >> a >> b >> c >> n;
        ll sum = a + b + c + n;
        if(sum % 3 != 0) {
            cout << "NO"<< endl;
            continue;
        }
        else{
            ll avg;
            avg = sum / 3;
            if(a > avg || b > avg || c > avg){
                cout << "NO"<<endl;
            }
            else cout << "YES" <<endl;
        }
    }

    return 0;
}
