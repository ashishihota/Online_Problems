#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll f, d;
  cin >> f >> d;
  vector<vector<ll>> v(d);
  for(int i = 0; i < d; i++){
    ll n;
    cin >> n;
    for(int j = 0; j < n; j++){
      ll x;
      cin >> x;
      v[--x].push_back(i);
    }
  }

}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
