#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  vector<ll> v(n);
  for(int i = 0; i < n; i++){
    cin >> v[i];
  }


  vector<ll> pre(n), pos(n);
  for(int i = 2; i < n; i++){
    pre[i] = v[i] - v[i - 1];
  }

  for(int i = n-1; i > 1; i++){
    pos[i] = pos[i] - pos[i-1];
  }


  cout << "\n pre and pos" << endl;
  for(auto i : pre) cout << i << " "; cout << endl;
  for(auto i : pos) cout << i << " "; cout << endl;

}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
