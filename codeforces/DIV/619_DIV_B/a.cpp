#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll num;
    cin >> num;
    while(num--){
        string a, b, c;
        cin >> a >> b >> c;
        bool flag = true;
        for(int i = 0; i < a.length(); i++){
            if(a[i] == b[i]){
                if(a[i] != c[i]){
                    flag = false;
                    break;
                }
                else continue;
            }
            else if((a[i] == c[i]) || b[i] == c[i]){
                continue;
            }
            else {
                flag = false;
                break;
            }
        }
        if(flag) cout << "YES" << endl;
        else cout << "NO" << endl;
    }
    return 0;
}
