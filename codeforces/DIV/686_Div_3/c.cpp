#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  vector<ll> v(n), vv;;
  multiset<ll> ss;
  for(int i = 0; i < n; i++){
    cin >> v[i];
  }

  
  ll pre = -1;
  for(int i = 0; i < n; i++){
    if(v[i] == pre) continue;
    else {
      pre = v[i];
      vv.push_back(v[i]);
    };
  }

  v = vv;
  set<pair<ll,ll>> s;
  for(int i = 0; i < vv.size(); i++){
    ss.insert(v[i]);
  }

  if(ss.size() == 1){
    cout << 0 << endl;
    return;
  }


  ll ans = MOD;
 for(auto i: ss){
    //if(s.count(i.first) >= 1) continue;
    ll z = ss.count(i);
    if(v[0] == v[v.size()-1] && i == v[0]){
      s.insert(make_pair(i,z));
      z--;
    }
    else if(v[0] == i || v[v.size() -1] == i) {
      s.insert(make_pair(i,z));
    }
    else{
      z++;
      s.insert(make_pair(i,z)); 
    }
    ans = min(ans, z);
 }

 cout << ans << endl;

}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }  
    return 0;
}
