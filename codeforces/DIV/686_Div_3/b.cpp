#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  std::vector<ll> v(n);
  multiset<ll> st;
  for(int i = 0; i < n; i++){
    cin >> v[i];
    st.insert(v[i]);
  }
  ll ans  = -1;
  for(auto i: st){
    if(st.count(i) > 1)continue;
    else{
      ans = i;
      break;
    }
  }
  for(int i = 0; i < n; i++){
    if(v[i] == ans){
      cout << i+1 << endl;
      return;
    }
  }
  cout << "-1" << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }  
    return 0;
}
