typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

bool isPalindrome(string s){
  for(int i = 0; i < s.length()/2; i++){
    if(s[i] != s[s.length() - i - 1]) return false;
  }
  return true;
}

void solve(){
  string s;
  cin >> s;
  if(isPalindrome(s)){
    for(int i = 0; i < s.length() / 2; i++){
      if(s[i] != 'a'){
        cout << "YES" << endl <<  'a' + s << endl;
        return;
      }
    }
    cout << "NO" << endl;
    return;
  }
  else{
    ll pos;
    ll n = s.length();
     for(int i = 0; i < n/2; i++){
        if(s[i] != s[n - i - 1]){
          pos = i;
        }
     }
     string ss, sss;
     ss = s.substr(0, pos);
     sss = s.substr(pos + 1, n);
     ss += 'a';
     cout << "YES" << endl <<  ss + sss << endl;
  }
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
