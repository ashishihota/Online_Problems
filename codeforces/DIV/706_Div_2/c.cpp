void solve(){
  ll n, k;
  cin >> n >> k;
  string s;
  cin >> s;
  if((k * 2 ) + 1 > n){
    cout << "NO" << endl;
    return;
  }
  if(k == 0){
    cout << "YES" << endl;
    return;
  }
  ll len = k * 2 + 1;
  ll pos = n - len;

  if(len == n){
    for(int i = 0; i < n/2; i++){
      if(s[i] != s[n - i - 1]){
        cout << "NO" << endl;
        return;
      }
    }
    cout << "YES" << endl;
    return;
  }
  // cout << "len = " << len << endl;
  for(int i = 0; (i + len) < n; i++){
    if(s[i] != s[len + i- 1]){
      // cout << s[i] << " != " << s[len - 1] << "  -----  " << i << "  " << len-1<< endl;
      continue;
    }
    else{
      // cout << "Equal at i = " << i << " and len-1 = " << len + i - 1 << endl;
      bool flag = true;
      for(int j = 0; j < len; j++){
        if(s[ i + j] != s[len + i - j - 1]){
          // cout << "line 42" << s[j] << " " << s[len - j - 1] << endl;
          flag = false;
          break;
        }
      }
      if(flag){
        cout << "YES" << endl;
        return;
      }
    }
  }
  cout << "NO" << endl;
}



