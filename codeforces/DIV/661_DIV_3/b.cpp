#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while(t--){
        ll n; 
        cin >> n;
        int canmin = MOD, oramin = MOD;
        vector<int> candie(n), orange(n);
        for(int i = 0; i < n; i++){
            cin >> candie[i];
            canmin = min(canmin, candie[i]);
        }
        for(int i = 0; i < n; i++){
            cin >> orange[i];
            oramin = min(oramin, orange[i]);
        }
        for(int i = 0; i < n; i++){
            candie[i] = candie[i] - canmin;
            orange[i] = orange[i] - oramin;
        }
        ll ans =  0;
        for(int i = 0; i < n; i++){
            ans += max(candie[i], orange[i]);
        }
        cout << ans << endl;
    }
    return 0;
}
