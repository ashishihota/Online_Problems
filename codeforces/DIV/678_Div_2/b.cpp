#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

const int prime = 9679;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
	ll t;
	cin >> t;
	while(t--){
		ll n;
		cin >> n;
		if(n == 2){
			cout << 1 << " " << 1 << endl;
			cout << 1 << " " << 1 << endl;
			continue;
		}
		vector<vector<ll>> v(n, vector<ll>(n));
		ll tot9 = 9;
		ll tot4 = (n-2) * 4;
		ll left = prime - tot9 - tot4;
		for(int i = 0; i < n; i++){
			for(int j = 0; j < n; j++){
				if(i == j) cout << left << " ";
				else if((j == i+1) || (j == 0 && i == n-1)) cout << 9 << " ";
				else cout << 4 << " ";
			}
			cout << endl;
		}
	}
    return 0;
}
