#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  string s;
  cin >> s;
  ll len = s.length(), cnt = 0;
  ll sum = 0;
  reverse(s.begin(), s.end());
  for(int i = 0; i < len; i++){
    sum += pow(2, cnt) * (s[i] - '0');
    cnt++;
  }

  ll n = len;
  ll size = pow(2,n);
  std::vector<ll> v(size);
  for(int i = 0; i < size; i++){
    v[i] = sum^i;
  }


  ll ans = 0;
  //reverse(v.begin(), v.end());
  for(int i = 0; i < size; i++){
    for(int j = i+1; j < size; j++){
      if(v[i] > v[j])ans++;
    }
    ans %= 1000000007;
  }
  cout << ans << endl;

}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
