#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll t;
    cin >> t;
    while(t--){
        ll n;
        cin >> n;
        std::vector<ll> vn(n), vb(n);
        for(int i = 0; i < n; i++){
            cin >> vn[i];
        }
        for(int i = 0; i < n; i++){
            cin >> vb[i];
        }
        sort(vn.begin(), vn.end());
        sort(vb.begin(), vb.end());
        reverse(vb.begin(), vb.end());
        ll x = vb[0];
        for(int i = 0; i < n - 1; i++){
            vb[i] = vb[i + 1];
        }
        vb[n - 1] = x;
        for(int i = 0; i < n; i++){
            cout << vn[i] << " ";
        }
        cout << endl;
        for(int i = 0; i < n; i++){
            std::cout << vb[i] << " ";
        }
        cout << endl;
    }
    return 0;
}
