#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while(t--){
        ll n, k;
        cin >> n >> k;
        vector<int> v(n*k);
        for(auto& i : v) cin >> i;
        ll mid = ceil(n / 2);
        ll sum = 0;
        for(int i = 1; i <= k; i++){
            sum += v[n * k - (i * mid)];
        }
        cout << sum << endl;
    }
    return 0;
}
