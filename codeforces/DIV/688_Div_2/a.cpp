#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n, m; // m = left train, n = bottom train
  cin >> n >> m;
  set<ll> s1;
  for(int i = 0; i < n; i++){
    ll a;
    cin >> a;
    s1.insert(a);
  }
  ll cnt = 0;
  for(int i = 0; i < m; i++){
    ll a;
    cin >> a;
    if(s1.count(a) == 1) cnt++;
  }
  cout << cnt << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
