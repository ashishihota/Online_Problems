#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  cout << "hwllo" << endl;
  ll n, sum = 0, mx = -MOD;
  cin >> n;
  std::vector<ll> v(n), vv(n);
  vector<pair<int,int>> pr;
  for(auto& i : v)cin >> i;
  sort(v.begin(), v.end());
  for(int i = 1; i < n; i++){
    pr[i] = {i,abs(v[i] - v[i-1])};
    vv[i] = abs(v[i] - v[i-1]);
    if(vv[i] > mx){
      mx = vv[i];
    }
  }
  for(auto i : pr){
    if(i.second == mx){
      continue;
    }
    else sum += i.second;
  }
  cout << "sum = " << sum / (n-1);
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
