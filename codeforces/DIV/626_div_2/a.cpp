int main() {
    ll t;
    cin >> t;
    while(t--){
        ll n;
        bool flag = false;
        cin >> n;
        std::vector<ll> v(n), vv;
        for(int i = 0; i < n; i++){
            cin >> v[i];
        }
        for(int i = 0;  i <n ;i++){
            if(v[i] % 2 == 0){
            //    cout << "ans = ";
                cout << "1" << endl << (i + 1)<< endl;
                flag = true;
                break;
            }
        }
        if(flag) continue;
        for(int i = 0; i < n - 1; ++i){
            if((v[i] + v[i + 1]) % 2 == 0){
            //    cout << "ans  = ";
                cout << "2" << endl << (i +1) << " " << (i  + 2) << endl;
                flag = true;
                break;
            }
        }
        if(!flag) cout << "-1" << endl;
    }
    return 0;
}
