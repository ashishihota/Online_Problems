#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll n, mn = MOD, pos = -1;
    ll dec = 0, inc = 0;
    cin >> n;
    std::vector<ll> v(n + 1);
    for(int i = 1; i <= n; i++){
        cin >> v[i];
        if(v[i] < mn){
            mn = v[i];
            pos = i;
        }
    }
    for(int i = 1; i < n; i++){
        if(v[i] > v[i + 1]) dec++;
        else inc++;
    }
    if(dec > inc){
        for(int i = pos; i <= n; i++){
            v[i] = mn;
        }
        for(int i = pos; i > 1; i--){
            v[i] = min(v[i - 1], v[i + 1]);
        }
    }
    else{
        for(int i = pos; i <= n; i++){
            v[i] = min(v[i - 1], v[i + 1]);
        }
        for(int i = pos; i > 0; i++){
            v[i] = mn;
        }
    }
    for(auto i : v) cout << i << " ";
    return 0;
}
