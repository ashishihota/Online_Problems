#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll t;
    cin >> t;
    while(t--){
        ll cnt = 0;
        std::vector<ll> v(3);
        for(int i = 0; i < 3; i++){
            cin >> v[i];
        }
        sort(v.begin(), v.end());
        reverse(v.begin(), v.end());
        for(int i = 0; i < 3; i++){
            if(v[i] != 0){
                v[i]--;
                cnt++;
            }
        }
        if(v[0] != 0 && v[1] != 0){
            cnt++;
            v[0]--; v[1]--;
        }
        if(v[2] != 0 && v[1] != 0){
            cnt++;
            v[2]--; v[1]--;
        }
        if(v[0] != 0 && v[2] != 0){
            cnt++;
            v[0]--; v[2]--;
        }
        if(v[0]!=0 && v[1] !=0 && v[2] != 0){
            v[0]--;
            v[1]--;
            v[2]--;
            cnt++;
        }
        cout << cnt << endl;
    }
    return 0;
}
