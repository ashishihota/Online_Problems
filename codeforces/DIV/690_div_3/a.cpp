#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  std::vector<ll> v(n), vv;
  for(auto& i : v) cin >> i;
  int i = 0, j = n-1, bit = 1;
  while(i <= j){
    if(bit){
      bit ^= 1;
      vv.push_back(v[i]);
      i++;
    }
    else{
      bit ^= 1;
      vv.push_back(v[j]);
      j--;
    }
  }
  for(auto i : vv) cout <<  i << " ";
  cout << endl;
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
