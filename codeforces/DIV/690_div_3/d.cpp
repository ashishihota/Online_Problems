#include <bits/stdc++.h>

using namespace std;


typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n, mx = -MOD;
  cin >> n;
  set<ll> st;
  vector<ll> v(n);
  for(auto& i : v){
   cin >> i;
   st.insert(i);
  }

  if(st.size() == 1) {
    cout << 0 << endl;
    return;
  }


  ll index = -1;
  for(int i = 0; i < n; i++){
    if(v[i] > mx){
      mx = v[i];
      index = i;
    }
  }

  vector<ll> vv;
  ll sum = 0;
  
  for(int i = 0; i < n; i++){
    if(v[i] != mx){
      if(sum + v[i] > mx){
        vv.push_back(sum);
        sum = 0;
      }
      else{
        sum += v[i];
      }
    }
    else{
      vv.push_back(sum);
      sum = 0;
      vv.push_back(v[i]);
    }
  }
  
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
