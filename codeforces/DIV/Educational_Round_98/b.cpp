#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
	   ll t;
     cin >> t;
     while(t--){
      ll n, mx = -MOD, sum = 0;
      cin >> n;
      vector<ll> v(n),req(n), need(n), left(n), suff(n+1), pre(n);
      for(int i = 0; i < n; i++){
        cin >> v[i];
        mx = max(mx, v[i]);
      }

      for(int i = 0; i < n; i++){
        sum = sum + mx - v[i];
        pre[i] = sum;
      }

      sum = 0;
      for(int i = n - 1; i >= 0; i--){
        sum = sum + mx - v[i];
        suff[i] = sum;
      }

      sum = 0;
      mx = -MOD;
      need[0] = suff[1];
      need[n-1] = pre[n-2];
      for(int i = 1; i < n-1; i++){
        need[i] = pre[i-1] + suff[i+1];
      }

      for(auto i : need) mx = max(mx, i);

      for(auto i : need){
        if( i == mx ) i = 0;
      }

      cout << "------------\n"; 
      for(auto i : v) cout << i << " "; cout <<endl; 
      for(auto i : need) cout << i << " "; cout << endl;
      for(auto i : pre) cout <<  i << " "; cout << endl;
      for(auto i : suff) cout << i << " "; cout << endl << endl;

      mx  = -MOD;
      for(int i = 0; i < n; i++){
        req[i] = v[i] - need[i];
        mx = max(mx, req[i]);
      }
      
      cout << mx << endl;

     }
    return 0;
}
