#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while(t--){
      string s;
      cin >> s;
      ll ans =  0;
      ll p = 0, sb = 0;
      for(auto i : s){
        if(i == '('){
          p++;
        }
        else if(i == '['){
          sb++;
        }
        else if(i == ')' && p){
          ans++;
          p--;
        }
        else if(i == ']' && sb){
          ans++;
          sb--;
        } 
      }
      cout << ans << endl;
    }	
    return 0;
}
