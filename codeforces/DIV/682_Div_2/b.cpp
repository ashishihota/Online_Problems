#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  ll t;
  cin >> t;
  while(t--){
  	ll n;
  	cin >> n;
  	vector<ll> suf(n), v(n);
  	for(int i = 0; i < n; i++){
  		ll x;
  		cin >> x;
  		v[i] = pow(2,x);	
  	}

  	//16, 8, 1, 4, 2, 1
  	//4,  3, 0, 2, 1, 0
  	ll sum = 0;
  	for(int i = n - 1; i >= 0; i--){
  		sum += v[i];
  		suf[i] = sum;
  	}
  	bool flag = false;
  	sum = 0;
  	for(int i = 0; i < n - 1; i++){
  		sum += v[i]; 
  		if(sum == suf[i+1]){
  			flag = true;
  			break;
  		}
  	}
  	cout << flag ? "YES" : "NO";
  	cout << endl;
  }
  return 0;
}
