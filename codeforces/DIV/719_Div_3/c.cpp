#define repp(i,a,b) for(i=a;i<b;++i)


void solve(){
  ll n;
  cin >> n;
  ll nn = n * n;
  if(n == 1){
    cout << 1 << endl;
    return;
  }
  ll mid = (nn) / 2;
  vector<vector<ll>> vv;
  ll left = nn - mid;
  ll st = 1;
  ll cnt = 1;
  while(left--){
    cout << st << " ";
    st += 2;
    if(cnt == n){
      cout << endl;
      cnt = 0;
    }
    cnt++;
  }
  st = 2;
  while(mid--){
    if(cnt == n){
      cout << endl;
      cnt = 0;
    }
    cout << st << " ";
    st += 2;
    cnt++;
  }
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while(t--){
      solve();
    }	 
    return 0;
}
