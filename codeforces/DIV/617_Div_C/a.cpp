using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll num;
    cin >> num;
    while (num--) {
        ll n, odd = 0, even = 0;
        cin >> n;
        std::vector<ll> v(n);
        for(int i = 0;i < n; i++){
            cin >> v[i];
            if(v[i] % 2 == 0)even++;
            else if(v[i] %2 != 0) odd++;
        }
        if((even != 0) && (odd != 0)) cout << "YES" << endl;
        else if((even == 0) && (odd % 2 != 0 )) cout << "YES" << endl;
        else cout << "NO" << endl;
    }
    return 0;
}
