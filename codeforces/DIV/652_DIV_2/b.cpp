#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while(t--){
        ll n;
        cin >> n;
        ll cnt = 1, cntt = n;
        string s, ss = "", sa = "";
        cin >> s;
        bool flag = true, fl = false;
        for(int i = 0; i < n - 1 ; ++i){
            if(s[i] == '0') fl = true;
            if( s[i] == '1' && s[i+1] == '0'){
                break;
            }
            else{
                cnt++;
                if(fl) sa += s[i];
            }
        }
        fl = false;
        for(int i = n -1; i > 0; i--){
            if(s[i] == '1') fl = true;
            if( s[i] == '0' && s[i-1] == '1'){
                break;
            }else{
                cntt--;
                if(fl) 
                ss += s[i];
            }
        }
        if(cnt == n) cout << s << endl;
        else{
            cout << "ans = ";
            cout << sa + '0' + ss << endl;
        }
    }
    return 0;
}
