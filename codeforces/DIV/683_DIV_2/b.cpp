#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
	ios::sync_with_stdio(false);
  cin.tie(nullptr);
	ll t;
  cin >> t;
  while(t--){
    ll n, m;
    cin >> n >> m;
    ll v[n][m];
    ll sum = 0, summ = 0, mn = MOD;
    bool haszero = false;
    int hasneg = 0;
    for(int i = 0; i < n; i++){
      for(int j = 0; j < m; j++){
        cin >> v[i][j];
     //   summ += abs(v[i][j]);
        if(v[i][j] == 0) haszero = true;
        mn = min(mn, abs(v[i][j]));
        if(v[i][j] < 0) {
          hasneg++;
        }
      }
    }

    //cout << "\n the value of summ = " << summ << "\n the value of mn = " << mn  << endl;
    if(haszero || !(hasneg % 2)){
      for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
          sum += abs(v[i][j]);
        }
      }
      cout << sum << endl;
    }
    else if(!hasneg){
      for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
          sum += v[i][j];
        }
      }
      cout << sum << endl;
    }
    else{
      bool notseen = true;
      for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
          sum += abs(v[i][j]);
        }
      }  
      cout << sum - 2 * mn << endl;
    }

  }
  return 0;
}
