#include <bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main(){
	ll t;
	cin >> t;
	while(t--){
		ll n;
		cin >> n;
		vector<vector<ll>> v(n);
		for(int i = 0; i < n; i++){
			ll l;
			cin >> l;
			vector<ll> vv(l);
			for(int j = 0; j < l; j++) cin >> vv[i];
			sort(vv.begin(), vv.end());
			v[i] = vv;
		}
		ll cnt = n;
		sort(v.begin(), v.end());
		vector<int> p1(n, 1), p2(n, 1);
		for(int i = 0; i < n; i++){
			bool flag = false;
			for(int j = 0; j < v[i].size(); j++){
				if(p1[j]){
					p1[j] = 0;
					p2[i] = 0;
					flag = true;
					cnt--;
				}
			}
			if(flag) continue;
		}
		if(cnt == 0){
			cout << "OPTIMAL" << endl;
			continue;
		}
		cout << "IMPROVE" << endl;
		for(auto i : p1){
			if(i != 0) cout << i << " ";
		}
		for(auto i: p2){
			if(i != 0) cout << i << endl;
		}
	}
    return 0;
}

