#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll num;
    cin >> num;
    while (num--) {
        ll len, g, b, days = 0, cnt= 0;
        cin >> len >> g >> b;
        ll mid = (len + 1) / 2;
        if(len  - g <= 0){
            cout << len << endl;
            continue;
        }
        else {
            for(int i = mid; i >= 0;){
                if((len - b) <= 0){
                    days = days + g + b;
                    mid -= g;
                }
                else{
                    days += g + b;
                    mid -= g;
                }
            }
        }
        cout << days << endl;
    }
    return 0;
}
