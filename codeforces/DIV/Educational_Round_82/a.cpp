#include<bits/stdc++.h>

using namespace std;


typedef long long ll;
typedef pair<int, int> pii;
const int MOD = 1e9 + 7;

#define rep(i,b) for(i=0;i<b;++i)
#define repp(i,a,b) for(i=a;i<b;++i)

int main() {
    ll num;
    cin >> num;
    while(num--){
        ll n, frst = -1, lst =-1;
        string s;
        cin >> s;
        for(int i = 0; i < s.length(); i++){
            if(s[i] == '1'){
                frst = i;
                break;
            }
        }
        for(int i = s.length() - 1; i >=0 ; i--){
            if(s[i] == '1'){
                lst = i;
                break;
            }
        }
        ll cnt = 0;
        for(int i = frst; i < lst; i++){
            if(s[i] == '0') cnt++;
        }
        cout << cnt << endl;
    }
    return 0;
}
